package maps;

public class Auto {

	private String brend;
	private int years;
	
	public Auto(String brend, int years) {
		super();
		this.brend = brend;
		this.years = years;
	}
	public String getBrend() {
		return brend;
	}
	public void setBrend(String brend) {
		this.brend = brend;
	}
	public int getYears() {
		return years;
	}
	public void setYears(int years) {
		this.years = years;
	}
	
	
	
}
